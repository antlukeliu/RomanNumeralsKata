//Objective of this is to take in a number and convert it Roman Numeral
// 1 = "I"
// 3 = "III"
// 9 = "IX"
// 1066 = "MLXVI"
// 1989 = "MCMLXXXIX"



public class RomanNumeralsConverter {

	public String convert(String number) {
		
		if(number.equals("1")){
		return "I";
		}else return "II";
  }
}
